'use strict';

function buildSeminarResponseCode() {
    return [
        '// ===================================================',
        '// WF-01C: getSeminars - 협회 세미나/이벤트 목록 응답',
        '// ===================================================',
        '',
        'const requestItem = $(\'Webhook\').first().json || {};',
        'const response = $input.first().json;',
        'const seminarList = Array.isArray(response.list) ? response.list : [];',
        'const body = requestItem.body || {};',
        'const year = Number(body.year || 0);',
        'const month = Number(body.month || 0);',
        'const affiliationCode = String(body.affiliationCode || body.affiliation_code || \'\').trim();',
        'const limit = Math.max(1, Math.min(60, Number(body.limit || 24) || 24));',
        '',
        'function pad2(value) {',
        "  return String(value).padStart(2, '0');",
        '}',
        '',
        'function toUtcDate(value) {',
        "  const text = String(value || '').trim();",
        '  const parts = text.split(\'-\');',
        '  if (parts.length !== 3) return null;',
        '  return new Date(Date.UTC(Number(parts[0]), Number(parts[1]) - 1, Number(parts[2])));',
        '}',
        '',
        'function getTodayKst() {',
        '  const now = new Date();',
        '  const utcTime = now.getTime() + now.getTimezoneOffset() * 60000;',
        '  return new Date(utcTime + 9 * 60 * 60000);',
        '}',
        '',
        'function diffDays(dateA, dateB) {',
        '  if (!dateA || !dateB) return null;',
        '  return Math.round((dateA.getTime() - dateB.getTime()) / 86400000);',
        '}',
        '',
        'function normalizeStatus(raw) {',
        "  const upper = String(raw || '').replace(/\\s+/g, ' ').trim().toUpperCase();",
        "  if (!upper) return 'DRAFT';",
        "  if (upper === 'ACTIVE' || upper === 'OPEN') return 'ACTIVE';",
        "  if (upper === 'PAUSED') return 'PAUSED';",
        "  if (upper === 'CLOSED' || upper === 'COMPLETED') return 'COMPLETED';",
        '  return upper;',
        '}',
        '',
        'function monthLabel(monthNumber) {',
        "  return monthNumber ? (String(monthNumber) + '월') : '-';",
        '}',
        '',
        'function seasonLabel(monthNumber) {',
        "  if (monthNumber === 2 || monthNumber === 3) return '졸업 시즌';",
        "  if (monthNumber === 4 || monthNumber === 5) return '가정의 달';",
        "  if (monthNumber === 5 || monthNumber === 6) return '웨딩 시즌';",
        "  if (monthNumber === 8 || monthNumber === 9) return '추석 시즌';",
        "  if (monthNumber === 11 || monthNumber === 12) return '크리스마스 시즌';",
        "  if (monthNumber === 7) return '방학 시즌';",
        "  return '정기 세미나';",
        '}',
        '',
        'const today = toUtcDate(getTodayKst().toISOString().slice(0, 10));',
        '',
        'const rows = seminarList.map((item) => {',
        '  const date = toUtcDate(item.seminar_date || \'\');',
        '  const monthNumber = date ? (date.getUTCMonth() + 1) : 0;',
        '  return {',
        "    seminar_id: item.seminar_id || '',",
        "    affiliation_code: item.affiliation_code || '',",
        "    title: item.title || '',",
        "    description: item.description || '',",
        "    seminar_date: item.seminar_date || '',",
        "    seminar_time: item.seminar_time || '',",
        "    location: item.location || '',",
        '    capacity: Number(item.capacity || 0),',
        '    status: normalizeStatus(item.status || \'\'),',
        "    image_url: item.image_url || '',",
        '    month: monthNumber,',
        '    month_label: monthLabel(monthNumber),',
        '    season_label: seasonLabel(monthNumber),',
        '    d_day: date ? diffDays(date, today) : null',
        '  };',
        '});',
        '',
        'let filtered = rows.filter((item) => item.status === \'ACTIVE\');',
        'if (year > 0) {',
        '  filtered = filtered.filter((item) => String(item.seminar_date || \'\').slice(0, 4) === String(year));',
        '}',
        'if (month > 0) {',
        '  filtered = filtered.filter((item) => item.month === month);',
        '}',
        'if (affiliationCode) {',
        '  filtered = filtered.filter((item) => item.affiliation_code === affiliationCode);',
        '}',
        '',
        'filtered.sort((a, b) => {',
        '  const dateA = String(a.seminar_date || \'\') + \' \' + String(a.seminar_time || \'\');',
        '  const dateB = String(b.seminar_date || \'\') + \' \' + String(b.seminar_time || \'\');',
        '  return dateA.localeCompare(dateB);',
        '});',
        '',
        'const limited = filtered.slice(0, limit);',
        'const monthsCovered = [];',
        'const seenMonths = {};',
        'limited.forEach((item) => {',
        '  if (item.month && !seenMonths[item.month]) {',
        '    seenMonths[item.month] = true;',
        '    monthsCovered.push(item.month);',
        '  }',
        '});',
        '',
        'return [{',
        '  json: {',
        '    success: true,',
        '    data: limited,',
        '    total: filtered.length,',
        '    summary: {',
        '      year: year || null,',
        '      month: month || null,',
        '      affiliation_code: affiliationCode || \'\',',
        '      months_covered: monthsCovered,',
        '      upcoming_count: limited.filter((item) => item.d_day !== null && item.d_day >= 0).length,',
        '      due_in_14_days: limited.filter((item) => item.d_day === 14).length',
        '    },',
        '    timestamp: new Date().toISOString()',
        '  }',
        '}];'
    ].join('\n');
}

module.exports = {
    buildSeminarResponseCode: buildSeminarResponseCode
};
