#!/usr/bin/env node
'use strict';

var fs = require('fs');
var path = require('path');

var REPO_ROOT = path.resolve(__dirname, '..');
var OUTPUT_PATH = path.join(REPO_ROOT, '파트너클래스', 'n8n-workflows', 'WF-SUB-subscription-kit-pilot.json');

function writeJson(filePath, value) {
    fs.writeFileSync(filePath, JSON.stringify(value, null, 2) + '\n', 'utf8');
}

function buildHandlerCode() {
    return [
        "const https = require('https');",
        '',
        "const input = $input.first().json || {};",
        "const body = input.body || input || {};",
        "const action = String(body.action || '').trim();",
        '',
        'function requestJson(hostname, path, method, bodyValue, headers) {',
        '  return new Promise((resolve, reject) => {',
        '    const options = {',
        "      hostname: hostname,",
        "      path: path,",
        "      method: method,",
        "      headers: headers || {}",
        '    };',
        '    const req = https.request(options, (res) => {',
        "      let data = '';",
        "      res.on('data', chunk => data += chunk);",
        "      res.on('end', () => {",
        '        let parsed = null;',
        '        try { parsed = data ? JSON.parse(data) : null; } catch (error) { parsed = { raw: data }; }',
        '        if (res.statusCode < 200 || res.statusCode >= 300) {',
        "          reject(new Error(method + ' ' + path + ' failed: ' + res.statusCode + ' ' + JSON.stringify(parsed)));",
        '          return;',
        '        }',
        '        resolve(parsed);',
        '      });',
        '    });',
        "    req.on('error', reject);",
        '    if (typeof bodyValue !== "undefined") {',
        '      req.write(JSON.stringify(bodyValue));',
        '    }',
        '    req.end();',
        '  });',
        '}',
        '',
        'function metaRequest(method, suffix, bodyValue) {',
        "  return requestJson('nocodb.pressco21.com', '/api/v2/meta/bases/' + $env.NOCODB_PROJECT_ID + suffix, method, bodyValue, {",
        "    'xc-token': $env.NOCODB_API_TOKEN,",
        "    'Content-Type': 'application/json'",
        '  });',
        '}',
        '',
        'function dataRequest(tableId, method, suffix, bodyValue) {',
        "  return requestJson('nocodb.pressco21.com', '/api/v1/db/data/noco/' + $env.NOCODB_PROJECT_ID + '/' + tableId + (suffix || ''), method, bodyValue, {",
        "    'xc-token': $env.NOCODB_API_TOKEN,",
        "    'Content-Type': 'application/json'",
        '  });',
        '}',
        '',
        'async function findSubscriptionsTableId() {',
        "  const result = await metaRequest('GET', '/tables');",
        "  const list = Array.isArray(result.list) ? result.list : [];",
        "  const match = list.find((item) => item && item.title === 'tbl_Subscriptions');",
        "  return match ? match.id : '';",
        '}',
        '',
        'function sanitizeText(value, maxLen) {',
        "  const text = String(value || '').replace(/[\\u0000-\\b\\u000b\\f\\u000e-\\u001f]/g, '').trim();",
        '  return text.substring(0, maxLen || 200);',
        '}',
        '',
        'function toNumber(value, fallback) {',
        '  const num = Number(value);',
        '  return isNaN(num) ? (fallback || 0) : num;',
        '}',
        '',
        'function parseArray(value) {',
        '  if (Array.isArray(value)) return value;',
        "  if (!value) return [];",
        '  try {',
        '    const parsed = JSON.parse(value);',
        '    return Array.isArray(parsed) ? parsed : [];',
        '  } catch (error) {',
        '    return [];',
        '  }',
        '}',
        '',
        'function encodeWhereValue(value) {',
        "  return encodeURIComponent(String(value || ''));",
        '}',
        '',
        'function isValidDate(value) {',
        "  return /^\\d{4}-\\d{2}-\\d{2}$/.test(String(value || ''));",
        '}',
        '',
        'function isValidMonth(value) {',
        "  return /^\\d{4}-\\d{2}$/.test(String(value || ''));",
        '}',
        '',
        'function isValidEmail(value) {',
        "  return /^[^\\s@]+@[^\\s@]+\\.[^\\s@]+$/.test(String(value || '').trim());",
        '}',
        '',
        'function nowIso() {',
        '  return new Date().toISOString();',
        '}',
        '',
        'function todayString() {',
        '  const now = new Date();',
        "  return now.getFullYear() + '-' + String(now.getMonth() + 1).padStart(2, '0') + '-' + String(now.getDate()).padStart(2, '0');",
        '}',
        '',
        'function createSubscriptionId() {',
        '  const now = new Date();',
        '  const stamp = String(now.getFullYear())',
        "    + String(now.getMonth() + 1).padStart(2, '0')",
        "    + String(now.getDate()).padStart(2, '0')",
        "    + String(now.getHours()).padStart(2, '0')",
        "    + String(now.getMinutes()).padStart(2, '0')",
        "    + String(now.getSeconds()).padStart(2, '0');",
        "  const rand = String(Math.floor(Math.random() * 9000) + 1000);",
        "  return 'SUBS_' + stamp + '_' + rand;",
        '}',
        '',
        'function formatMonth(dateText) {',
        '  const value = isValidDate(dateText) ? dateText : todayString();',
        '  return String(value).slice(0, 7);',
        '}',
        '',
        'function computeNextOrderDate(deliveryDay, baseDateText, cycleMonths) {',
        '  const day = Math.min(Math.max(parseInt(deliveryDay, 10) || 5, 1), 28);',
        '  const monthsToAdd = Math.max(parseInt(cycleMonths, 10) || 1, 1);',
        '  const base = isValidDate(baseDateText) ? new Date(baseDateText + "T00:00:00+09:00") : new Date();',
        '  const candidate = new Date(base.getFullYear(), base.getMonth() + monthsToAdd, 1);',
        '  const maxDay = new Date(candidate.getFullYear(), candidate.getMonth() + 1, 0).getDate();',
        '  candidate.setDate(Math.min(day, maxDay));',
        "  return candidate.getFullYear() + '-' + String(candidate.getMonth() + 1).padStart(2, '0') + '-' + String(candidate.getDate()).padStart(2, '0');",
        '}',
        '',
        'function normalizePreviewItems(value) {',
        '  const list = parseArray(value);',
        '  return list.slice(0, 5).map((item) => sanitizeText(item, 40)).filter(Boolean);',
        '}',
        '',
        'function extractBrandUid(raw) {',
        "  const text = String(raw || '').replace(/\\s+/g, '').trim();",
        "  const match = text.match(/[?&]branduid=([^&#]+)/i);",
        '  if (match && match[1]) return decodeURIComponent(match[1]);',
        "  if (/^[A-Za-z0-9_-]{4,64}$/.test(text)) return text;",
        "  return '';",
        '}',
        '',
        'function mapSubscriptionRow(row) {',
        '  return {',
        "    subscription_id: row.subscription_id || '',",
        "    member_id: row.member_id || '',",
        "    class_id: row.class_id || '',",
        "    class_name: row.class_name || '',",
        "    partner_code: row.partner_code || '',",
        "    partner_name: row.partner_name || '',",
        "    kit_bundle_branduid: row.kit_bundle_branduid || '',",
        '    regular_price: toNumber(row.regular_price, 0),',
        '    subscriber_price: toNumber(row.subscriber_price, 0),',
        '    preview_items: normalizePreviewItems(row.preview_items_json || "[]"),',
        '    delivery_day: toNumber(row.delivery_day, 5),',
        "    status: row.status || '',",
        "    next_order_date: row.next_order_date || '',",
        "    last_order_date: row.last_order_date || '',",
        "    last_order_ref: row.last_order_ref || '',",
        "    last_batch_month: row.last_batch_month || '',",
        '    order_count: toNumber(row.order_count, 0),',
        "    shipping_zipcode: row.shipping_zipcode || '',",
        "    shipping_address1: row.shipping_address1 || '',",
        "    shipping_address2: row.shipping_address2 || '',",
        "    cancelled_at: row.cancelled_at || ''",
        '  };',
        '}',
        '',
        'async function listSubscriptions(tableId, memberId) {',
        "  const query = '?where=(member_id,eq,' + encodeWhereValue(memberId) + ')&sort=-CreatedAt&limit=100';",
        "  const result = await dataRequest(tableId, 'GET', query);",
        "  return (result.list || []).map(mapSubscriptionRow);",
        '}',
        '',
        'function splitSubscriptions(list) {',
        '  const active = [];',
        '  const inactive = [];',
        '  let totalSavings = 0;',
        '  let i = 0;',
        '',
        '  for (i = 0; i < list.length; i += 1) {',
        "    if (String(list[i].status || '') === 'ACTIVE') {",
        '      active.push(list[i]);',
        '      totalSavings += Math.max((list[i].regular_price || 0) - (list[i].subscriber_price || 0), 0);',
        '    } else {',
        '      inactive.push(list[i]);',
        '    }',
        '  }',
        '',
        '  return { active, inactive, totalSavings };',
        '}',
        '',
        'async function createSubscription(tableId) {',
        "  const memberId = sanitizeText(body.member_id, 100);",
        "  const classId = sanitizeText(body.class_id, 100);",
        "  const className = sanitizeText(body.class_name, 120);",
        "  const memberName = sanitizeText(body.member_name, 60);",
        "  const memberEmail = sanitizeText(body.member_email, 120);",
        "  const memberPhone = sanitizeText(body.member_phone, 40);",
        "  const partnerCode = sanitizeText(body.partner_code, 60);",
        "  const partnerName = sanitizeText(body.partner_name, 60);",
        '  const kitBundleBrandUid = extractBrandUid(body.kit_bundle_branduid || "");',
        '  const regularPrice = Math.max(toNumber(body.regular_price, 0), 0);',
        '  const subscriberPrice = Math.max(toNumber(body.subscriber_price, 0), 0);',
        '  const deliveryDay = Math.min(Math.max(parseInt(body.delivery_day, 10) || 5, 1), 28);',
        '  const nextOrderDate = isValidDate(body.next_order_date) ? body.next_order_date : computeNextOrderDate(deliveryDay, todayString(), 1);',
        '  const previewItems = normalizePreviewItems(body.preview_items || []);',
        "  const zipcode = sanitizeText(body.shipping_zipcode, 20);",
        "  const address1 = sanitizeText(body.shipping_address1, 180);",
        "  const address2 = sanitizeText(body.shipping_address2, 180);",
        "  const notes = sanitizeText(body.notes, 300);",
        '',
        '  if (!memberId || !classId || !className || !memberName || !memberPhone || !kitBundleBrandUid || !address1 || !zipcode) {',
        "    return { success: false, message: '필수 항목이 누락되었습니다.', statusCode: 400 };",
        '  }',
        '  if (!isValidEmail(memberEmail)) {',
        "    return { success: false, message: '이메일 형식이 올바르지 않습니다.', statusCode: 400 };",
        '  }',
        '',
        "  const duplicateWhere = '?where=(member_id,eq,' + encodeWhereValue(memberId) + ')~and(class_id,eq,' + encodeWhereValue(classId) + ')~and(status,eq,ACTIVE)&limit=1';",
        "  const duplicate = await dataRequest(tableId, 'GET', duplicateWhere);",
        '  if (duplicate.list && duplicate.list.length > 0) {',
        "    return { success: false, message: '이미 진행 중인 구독이 있습니다.', statusCode: 409 };",
        '  }',
        '',
        '  const payload = {',
        '    subscription_id: createSubscriptionId(),',
        '    member_id: memberId,',
        '    member_name: memberName,',
        '    member_email: memberEmail,',
        '    member_phone: memberPhone,',
        '    class_id: classId,',
        '    class_name: className,',
        '    partner_code: partnerCode,',
        '    partner_name: partnerName,',
        '    kit_bundle_branduid: kitBundleBrandUid,',
        '    regular_price: regularPrice,',
        '    subscriber_price: subscriberPrice,',
        '    preview_items_json: JSON.stringify(previewItems),',
        '    delivery_day: deliveryDay,',
        '    cycle_months: 1,',
        "    status: 'ACTIVE',",
        '    next_order_date: nextOrderDate,',
        "    last_order_date: '',",
        "    last_order_ref: '',",
        "    last_batch_month: '',",
        '    order_count: 0,',
        '    shipping_zipcode: zipcode,',
        '    shipping_address1: address1,',
        '    shipping_address2: address2,',
        '    started_at: nowIso(),',
        "    cancelled_at: '',",
        '    notes: notes',
        '  };',
        '',
        "  const created = await dataRequest(tableId, 'POST', '', payload);",
        '  return {',
        '    success: true,',
        "    message: 'subscription created',",
        '    data: mapSubscriptionRow(created),',
        '    statusCode: 201',
        '  };',
        '}',
        '',
        'async function cancelSubscription(tableId) {',
        "  const memberId = sanitizeText(body.member_id, 100);",
        "  const subscriptionId = sanitizeText(body.subscription_id, 120);",
        "  const where = '?where=(member_id,eq,' + encodeWhereValue(memberId) + ')~and(subscription_id,eq,' + encodeWhereValue(subscriptionId) + ')&limit=1';",
        "  const found = await dataRequest(tableId, 'GET', where);",
        "  const row = found.list && found.list[0] ? found.list[0] : null;",
        '',
        '  if (!row) {',
        "    return { success: false, message: '구독 정보를 찾지 못했습니다.', statusCode: 404 };",
        '  }',
        "  if (String(row.status || '') !== 'ACTIVE') {",
        "    return { success: false, message: '이미 해지된 구독입니다.', statusCode: 409 };",
        '  }',
        '',
        "  const patched = await dataRequest(tableId, 'PATCH', '/' + row.Id, { status: 'CANCELLED', cancelled_at: nowIso() });",
        '  return {',
        '    success: true,',
        "    message: 'subscription cancelled',",
        '    data: mapSubscriptionRow(patched),',
        '    statusCode: 200',
        '  };',
        '}',
        '',
        'async function runMonthlyBatch(tableId) {',
        "  const targetMonth = isValidMonth(body.month) ? body.month : formatMonth(body.target_date);",
        "  const asOfDate = isValidDate(body.target_date) ? body.target_date : (targetMonth + '-28');",
        "  const filterMemberId = sanitizeText(body.member_id, 100);",
        '  const dryRun = body.dry_run === true || body.dry_run === 1 || body.dry_run === "1" || body.dry_run === "true";',
        "  let query = '?where=(status,eq,ACTIVE)&limit=200';",
        '  let rows = [];',
        '  let dueRows = [];',
        '  let generated = [];',
        '  let i = 0;',
        '',
        '  if (filterMemberId) {',
        "    query = '?where=(status,eq,ACTIVE)~and(member_id,eq,' + encodeWhereValue(filterMemberId) + ')&limit=200';",
        '  }',
        '',
        "  rows = (await dataRequest(tableId, 'GET', query)).list || [];",
        '  dueRows = rows.filter((row) => {',
        "    const nextDate = String(row.next_order_date || '').substring(0, 10);",
        "    const lastBatchMonth = String(row.last_batch_month || '');",
        '    return nextDate && nextDate <= asOfDate && lastBatchMonth !== targetMonth;',
        '  });',
        '',
        '  for (i = 0; i < dueRows.length; i += 1) {',
        '    const row = dueRows[i];',
        "    const orderRef = 'SUBORD_' + targetMonth.replace('-', '') + '_' + sanitizeText(row.subscription_id, 120);",
        '    const nextOrderDate = computeNextOrderDate(row.delivery_day, row.next_order_date || asOfDate, row.cycle_months || 1);',
        '',
        '    generated.push({',
        "      subscription_id: row.subscription_id || '',",
        "      class_id: row.class_id || '',",
        "      class_name: row.class_name || '',",
        '      next_order_date: nextOrderDate,',
        '      last_order_ref: orderRef',
        '    });',
        '',
        '    if (!dryRun) {',
        "      await dataRequest(tableId, 'PATCH', '/' + row.Id, {",
        '        last_order_date: asOfDate,',
        '        last_order_ref: orderRef,',
        '        last_batch_month: targetMonth,',
        '        next_order_date: nextOrderDate,',
        '        order_count: Math.max(toNumber(row.order_count, 0), 0) + 1',
        '      });',
        '    }',
        '  }',
        '',
        '  return {',
        '    success: true,',
        '    message: dryRun ? "batch dry run complete" : "batch complete",',
        '    data: {',
        '      month: targetMonth,',
        '      target_date: asOfDate,',
        '      dry_run: dryRun,',
        '      total_candidates: rows.length,',
        '      generated_count: generated.length,',
        '      generated: generated',
        '    },',
        '    statusCode: 200',
        '  };',
        '}',
        '',
        'try {',
        '  if (!action) {',
        "    return [{ json: { success: false, message: 'action required', statusCode: 400 } }];",
        '  }',
        '',
        '  const tableId = await findSubscriptionsTableId();',
        '  if (!tableId) {',
        "    return [{ json: { success: false, message: 'tbl_Subscriptions not found', statusCode: 500 } }];",
        '  }',
        '',
        "  if (action === 'listSubscriptions') {",
        "    const memberId = sanitizeText(body.member_id, 100);",
        '    if (!memberId) {',
        "      return [{ json: { success: false, message: 'member_id required', statusCode: 400 } }];",
        '    }',
        '    const list = await listSubscriptions(tableId, memberId);',
        '    const grouped = splitSubscriptions(list);',
        '    return [{ json: {',
        '      success: true,',
        "      message: 'ok',",
        '      data: {',
        '        active: grouped.active,',
        '        inactive: grouped.inactive,',
        '        total_savings: grouped.totalSavings',
        '      },',
        '      statusCode: 200',
        '    } }];',
        '  }',
        '',
        "  if (action === 'createSubscription') {",
        '    return [{ json: await createSubscription(tableId) }];',
        '  }',
        '',
        "  if (action === 'cancelSubscription') {",
        '    return [{ json: await cancelSubscription(tableId) }];',
        '  }',
        '',
        "  if (action === 'runMonthlyBatch') {",
        '    return [{ json: await runMonthlyBatch(tableId) }];',
        '  }',
        '',
        "  return [{ json: { success: false, message: 'unknown action', statusCode: 400 } }];",
        '} catch (error) {',
        "  return [{ json: { success: false, message: 'Internal error: ' + error.message, statusCode: 500 } }];",
        '}'
    ].join('\n');
}

function buildWorkflow() {
    return {
        name: 'WF-SUB Subscription Kit Pilot',
        nodes: [
            {
                parameters: {
                    httpMethod: 'POST',
                    path: 'subscription-kit',
                    responseMode: 'responseNode',
                    options: {}
                },
                id: 'wfsub-webhook',
                name: 'Webhook',
                type: 'n8n-nodes-base.webhook',
                typeVersion: 2,
                position: [220, 220],
                webhookId: 'subscription-kit'
            },
            {
                parameters: {
                    jsCode: buildHandlerCode()
                },
                id: 'wfsub-handler',
                name: 'Handle Request',
                type: 'n8n-nodes-base.code',
                typeVersion: 2,
                position: [480, 220]
            },
            {
                parameters: {
                    respondWith: 'json',
                    responseBody: '={{ JSON.stringify({success: $json.success, message: $json.message, data: $json.data}) }}',
                    options: {
                        responseCode: '={{ $json.statusCode || 200 }}'
                    }
                },
                id: 'wfsub-respond',
                name: 'Respond',
                type: 'n8n-nodes-base.respondToWebhook',
                typeVersion: 1.1,
                position: [720, 220]
            },
            {
                parameters: {
                    rule: {
                        interval: [
                            {
                                field: 'cronExpression',
                                expression: '30 6 1 * *'
                            }
                        ]
                    }
                },
                id: 'wfsub-schedule',
                name: 'Schedule Monthly Batch',
                type: 'n8n-nodes-base.scheduleTrigger',
                typeVersion: 1.2,
                position: [220, 520]
            },
            {
                parameters: {
                    method: 'POST',
                    url: 'https://n8n.pressco21.com/webhook/subscription-kit',
                    sendBody: true,
                    specifyBody: 'json',
                    jsonBody: '={{ JSON.stringify({ action: "runMonthlyBatch", source: "schedule" }) }}',
                    options: {
                        timeout: 120000
                    }
                },
                id: 'wfsub-trigger-batch',
                name: 'Trigger Monthly Batch',
                type: 'n8n-nodes-base.httpRequest',
                typeVersion: 4.2,
                position: [480, 520]
            }
        ],
        connections: {
            Webhook: {
                main: [[{ node: 'Handle Request', type: 'main', index: 0 }]]
            },
            'Handle Request': {
                main: [[{ node: 'Respond', type: 'main', index: 0 }]]
            },
            'Schedule Monthly Batch': {
                main: [[{ node: 'Trigger Monthly Batch', type: 'main', index: 0 }]]
            }
        },
        settings: {
            executionOrder: 'v1'
        }
    };
}

function main() {
    writeJson(OUTPUT_PATH, buildWorkflow());
    console.log('saved ' + path.relative(REPO_ROOT, OUTPUT_PATH));
}

main();
