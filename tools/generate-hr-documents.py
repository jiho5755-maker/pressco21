FONT_BODY = '맑은 고딕'
SIZE_BODY = Pt(11)


def set_ea_font(run, font_name=FONT_BODY):
    run.font.name = font_name
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = OxmlElement('w:rFonts')
        rPr.append(rFonts)
    rFonts.set(qn('w:eastAsia'), font_name)
    rFonts.set(qn('w:ascii'), font_name)
    rFonts.set(qn('w:hAnsi'), font_name)


def new_doc():
    doc = Document()
    style = doc.styles['Normal']
    style.font.name = FONT_BODY
    style.font.size = SIZE_BODY
    return doc


def add_title(doc, text, size_pt=18):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(size_pt)
    set_ea_font(run)
    p.paragraph_format.space_after = Pt(8)
    return p


def add_heading(doc, text, level=1):
    size_map = {1: 14, 2: 13, 3: 12}
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(size_map.get(level, 11))
    set_ea_font(run)
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(6)
    return p


def add_body(doc, text, indent_cm=0, bold=False):
    p = doc.add_paragraph()
    if indent_cm:
        p.paragraph_format.left_indent = Cm(indent_cm)
    run = p.add_run(text)
    run.font.size = SIZE_BODY
    if bold:
        run.bold = True
    set_ea_font(run)
    p.paragraph_format.line_spacing = 1.4
    return p


def add_article(doc, title, lines):
    add_heading(doc, title, level=2)
    for ln in lines:
        if isinstance(ln, tuple):
            add_body(doc, ln[0], indent_cm=ln[1])
        else:
            add_body(doc, ln)


def make_table(doc, headers, rows, col_widths_cm=None):
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = 'Light Grid Accent 1'
    hdr_cells = t.rows[0].cells
    for i, h in enumerate(headers):
        hdr_cells[i].text = h
        for p in hdr_cells[i].paragraphs:
            for r in p.runs:
                r.bold = True
                set_ea_font(r)
    for row in rows:
        cells = t.add_row().cells
        for i, val in enumerate(row):
            cells[i].text = str(val)
            for p in cells[i].paragraphs:
                for r in p.runs:
                    r.font.size = Pt(10)
                    set_ea_font(r)
    if col_widths_cm:
        for row in t.rows:
            for i, w in enumerate(col_widths_cm):
                row.cells[i].width = Cm(w)
    return t


